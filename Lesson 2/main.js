//AND
let valida1 = true && true;
console.log("true && true" + valida1);

valida1 = true && false;
console.log("true && false" + valida1);

valida1 = false && true;
console.log("false && true" + valida1);

valida1 = false && false;
console.log("false && false" + valida1);


//OR
valida1 = true || true;
console.log("true || true: " + valida1);

valida1 = true || false;
console.log("true || false: " + valida1);

valida1 = false || true;
console.log("false || true: " + valida1);

valida1 = false || false;
console.log("false || false: " + valida1);


//NOT
valida1 = !true;
console.log("!true: " + valida1);

valida1 = !false;
console.log("!false: " + valida1);



